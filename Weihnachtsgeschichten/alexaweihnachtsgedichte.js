function buildSpeechletResponse(title, outputCard, outputSpeech, repromptText, shouldEndSession) {
    return {
        outputSpeech: {
            type: "PlainText",
            text: outputSpeech
        },
        card: {
            type: "Simple",
            title: title,
            content: outputCard
        },
        reprompt: {
            outputSpeech: {
                type: "PlainText",
                text: repromptText
            }
        },
        shouldEndSession: shouldEndSession
    };
}

